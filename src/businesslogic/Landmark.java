package businesslogic;

import java.util.*;

/**
 * 
 */
public class Landmark {

    /**
     * Default constructor
     */
    public Landmark() {
    }

    /**
     * X position of the landmark
     */
    public float posX;

    /**
     * Y position of the landmark
     */
    public float posY;

    /**
     * True : this crosspoint is a landmark
     * False : this crosspoint is not a landmark
     */
    public Boolean isLandmark;

}